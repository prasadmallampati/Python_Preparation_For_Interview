'''
What is an Operator?

An operator is a special symbol or keyword that performs an operation
on one or more operands (values or variables).

Example:
10 + 20

10 and 20 are operands.
+ is the operator.
'''

# Types of Operators in Python

'''
1. Arithmetic Operators
   Used to perform mathematical calculations.

   Operators:
   +   Addition
   -   Subtraction
   *   Multiplication
   /   Division
   //  Floor Division
   %   Modulus
   **  Exponentiation

----------------------------------------

2. Comparison (Relational) Operators
   Used to compare two values.
   Returns True or False.

   Operators:
   ==
   !=
   >
   <
   >=
   <=

----------------------------------------

3. Logical Operators
   Used to combine multiple conditions.

   Operators:
   and
   or
   not

----------------------------------------

4. Assignment Operators
   Used to assign or update values.

   Operators:
   =
   +=
   -=
   *=
   /=
   //=
   %=
   **=
   &=
   |=
   ^=
   >>=
   <<=

----------------------------------------

5. Bitwise Operators
   Operate on binary (bit-level) values.

   Operators:
   &
   |
   ^
   ~
   <<
   >>

----------------------------------------

6. Membership Operators
   Used to check whether a value exists in a sequence.

   Operators:
   in
   not in

----------------------------------------

7. Identity Operators
   Used to check whether two variables refer to the same object.

   Operators:
   is
   is not

----------------------------------------

8. Conditional (Ternary) Expression
   A concise way to write an if-else expression.

   Syntax:
   value_if_true if condition else value_if_false

Example:
result = "Pass" if marks >= 35 else "Fail"

'''




'''
quick summary 


| Operator Type           | Purpose                   | Operators                  |
| ----------------------- | ------------------------- | -------------------------- |
| Arithmetic              | Mathematical calculations | `+ - * / // % **`          |
| Comparison (Relational) | Compare values            | `== != > < >= <=`          |
| Logical                 | Combine conditions        | `and or not`               |
| Assignment              | Assign/update values      | `= += -= *= /= //= %= **=` |
| Bitwise                 | Operate on bits           | `& \| ^ ~ << >>`           |
| Membership              | Check membership          | `in`, `not in`             |
| Identity                | Compare object identity   | `is`, `is not`             |
| Conditional (Ternary)   | One-line `if-else`        | `x if condition else y`    |




'''
